/**
 * Class Option
 */
class Option
{
    /**
     * @var string
     */
    protected $value = '';

    /**
     * @var string
     */
    protected $label = '';

    /**
     * @return string
     */
    public function getValue(): string
    {
        return $this->value;
    }

    /**
     * @param string $value
     * @return Option Returns instance
     */
    public function setValue(string $value): self
    {
        $this->value = $value;

        return $this;
    }

    /**
     * @return string
     */
    public function getLabel(): string
    {
        return $this->label;
    }

    /**
     * Set the label
     *
     * @param string $label
     * @return Option Returns instance
     */
    public function setLabel(string $label): self
    {
        $this->label = $label;
        return $this;
    }
}
